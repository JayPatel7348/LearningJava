public class FindCharInString {
 // find character at different position ex. find char at 3 & length
    public static void main(String[] args) {
        String str1="this is a java";
        String str2="hi i am here";
        String str3="where are you";
        String str4="this is a demo";
        String str5="core java";

        System.out.println("Character at 3rd place is "+str1.charAt(3)+" & length of string is "+str1.length());
        System.out.println("Character at 2nd place is "+str2.charAt(2)+" & length of string is "+str2.length());
        System.out.println("Character at 0th place is "+str3.charAt(0)+" & length of string is "+str3.length());
        System.out.println("Character at 5th place is "+str4.charAt(5)+" & length of string is "+str4.length());
        System.out.println("Character at 6th place is "+str5.charAt(6)+" & length of string is "+str5.length());

    }
}
