public class StringSplitUsingstartWith {

    public static void main(String[] args) {
        String str="this is a text";
        /*
        Check whether the string starts with “thi”
        Check whether the string starts with “amo”
        Check whether the string contains “is”
        Check whether the string contains “man”
        */
        System.out.println(str.startsWith("thi"));
        System.out.println(str.startsWith("amo"));
        System.out.println(str.contains("is"));
        System.out.println(str.contains("man"));

    }
}
